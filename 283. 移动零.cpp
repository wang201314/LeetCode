//给定一个数组 nums，编写一个函数将所有 0 移动到数组的末尾，同时保持非零元素的相对顺序。
//
//请注意 ，必须在不复制数组的情况下原地对数组进行操作。
#include"leetcode.h"
using namespace std;
void Solution::moveZeroes(vector<int>& nums)
{
	int cur = 0;
	for (auto&num : nums)
	{
		if (num != 0)
		{
			nums[cur++] = num;
		}
	}
	while (cur < nums.size())
	{
		nums[cur++] = 0;
	}
}